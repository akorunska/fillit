#include "libft.h"

double	ft_pow(double x, int y)
{
	double	res;
	double	i;

	res = 1;
	i = 0;
	if (y < 0)
		return (0);
	while (i < y)
	{
		res *= x;
		i++;
	}
	return (res);
}
